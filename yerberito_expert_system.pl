format_planta_nombre(PlantaNombre, Formatted) :-
    PlantaNombre = Planta-Nombre,
    normalize_ascii(Nombre, NombreNorm),
    format(atom(Formatted), '- ~w: ~w', [Planta, NombreNorm]).

compare_planta_nombre(Order, Planta1Nombre, Planta2Nombre) :-
    Planta1Nombre = Planta1-_,
    Planta2Nombre = Planta2-_,
    compare(Order, Planta1, Planta2).

format_list_item(Item, Formatted) :-
    normalize_ascii(Item, ItemNorm),
    format(atom(Formatted), '- ~w', [ItemNorm]).

mostrar_error(Context, Error, Dialog) :-
    new(DisplayDialog, dialog('Error')),
    send(DisplayDialog, size, size(400, 200)),
    new(ErrorText, editor),
