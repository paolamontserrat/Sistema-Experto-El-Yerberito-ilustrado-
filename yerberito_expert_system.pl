% Helper predicate to normalize accented characters to ASCII
normalize_ascii(Input, Output) :-
    atom_chars(Input, Chars),
    maplist(normalize_char, Chars, NormalizedChars),
    atom_chars(Output, NormalizedChars).

normalize_char('á', 'a').
normalize_char('é', 'e').
normalize_char('í', 'i').
normalize_char('ó', 'o').
normalize_char('ú', 'u').
normalize_char('ñ', 'n').
normalize_char('Á', 'A').
normalize_char('É', 'E').
normalize_char('Í', 'I').
normalize_char('Ó', 'O').
normalize_char('Ú', 'U').
normalize_char('Ñ', 'N').
normalize_char(Char, Char) :- \+ member(Char, ['á','é','í','ó','ú','ñ','Á','É','Í','Ó','Ú','Ñ']).

% Helper predicate to get all plants from all sources
all_plants(Planta) :-
    (   planta_usos_medicinales(Planta, _)
    ;   imagen_planta(Planta, _),
        \+ planta_usos_medicinales(Planta, _)
    ;   nombre_cientifico(Planta, _),
        \+ planta_usos_medicinales(Planta, _),
        \+ imagen_planta(Planta, _)
    ;   caracteristicas_planta(Planta, _),
        \+ planta_usos_medicinales(Planta, _),
        \+ imagen_planta(Planta, _),
        \+ nombre_cientifico(Planta, _)
    ;   contraindicaciones_planta(Planta, _),
        \+ planta_usos_medicinales(Planta, _),
        \+ imagen_planta(Planta, _),
        \+ nombre_cientifico(Planta, _),
        \+ caracteristicas_planta(Planta, _)
    ).

% LISTA DE PLANTAS MEDICINALES
listar_plantas_medicinales(Plantas) :-
    setof(Planta, all_plants(Planta), Plantas).

% LISTA DE ELEMENTOS DE UNA PLANTA
elementos_planta(Planta, Componentes) :-
    atom(Planta),
    (   componentes_quimicos(Planta, Componentes) -> true
    ;   Componentes = []
    ).

% LISTA DE PLANTAS QUE PRODUCEN MEDICAMENTOS
plantas_producen_medicamentos(Plantas) :-
    setof(Planta, Componentes^(componentes_quimicos(Planta, Componentes), Componentes \= []), Plantas).

% MEDICAMENTOS PLANTA ESPECIFICA
medicamentos_planta(Planta, Medicamentos) :-
    atom(Planta),
    (   componentes_quimicos(Planta, Medicamentos) -> true
    ;   Medicamentos = []
    ).

% MEDICAMENTOS DE PLANTAS
medicamentos_de_plantas(Medicamentos) :-
    findall(Medicamento, (componentes_quimicos(_, Componentes), member(Medicamento, Componentes)), MedicamentosList),
    sort(MedicamentosList, Medicamentos).

% EFECTOS DE LAS MEDICINAS DE LAS PLANTAS
efectos_medicamentos_plantas(Efectos) :-
    findall(Efecto, (caracteristicas_planta(_, EfectosPlanta), member(Efecto, EfectosPlanta)), EfectosList),
    sort(EfectosList, Efectos).

% EFECTOS DE UN MEDICAMENTO
efectos_medicamento(Medicamento, Efectos) :-
    atom(Medicamento),
    findall(Efecto, (componentes_quimicos(Planta, Componentes), member(Medicamento, Componentes),
                     caracteristicas_planta(Planta, EfectosPlanta), member(Efecto, EfectosPlanta)), EfectosList),
    sort(EfectosList, Efectos).

% EFECTOS DE LAS PLANTAS
efectos_plantas(Efectos) :-
    efectos_medicamentos_plantas(Efectos).

% SIGNIFICADO DEL EFECTO
significado_accion(Efecto, Definicion) :-
    atom(Efecto),
    (   definicion_efecto(Efecto, DefTexto) ->
        normalize_ascii(DefTexto, Definicion)
    ;   Definicion = 'Definicion no encontrada'
    ).

% LISTA DE PLANTAS Y SUS EFECTOS
plantas_y_efectos(PlantaEfectos) :-
    findall(Planta-Efectos, (all_plants(Planta), caracteristicas_planta(Planta, Efectos)), PlantaEfectos).

% EFECTOS DE UNA PLANTA ESPECIFICA
efectos_planta_especifica(Planta, Efectos) :-
    atom(Planta),
    (   caracteristicas_planta(Planta, Efectos) -> true
    ;   Efectos = []
    ).

% LISTA DE PLANTAS ANALGESICAS
listar_plantas_analgesicas(Plantas) :-
    setof(Planta, planta_analgesica(Planta), Plantas).

% LISTA DE PLANTAS CON SU NOMBRE CIENTIFICO
plantas_con_nombre_cientifico(PlantaNombre) :-
    findall(Planta-Nombre, (all_plants(Planta), nombre_cientifico(Planta, NombreCient), normalize_ascii(NombreCient, Nombre)), PlantaNombre).

% ENFERMEDADES TRATADAS CON LAS PLANTAS
enfermedades_plantas(Enfermedades) :-
    findall(Enfermedad, (planta_usos_medicinales(_, Usos), member(Enfermedad, Usos)), EnfermedadesList),
    sort(EnfermedadesList, Enfermedades).

% ENFERMEDADES QUE TRATA UNA PLANTA ESPECIFICA
enfermedades_planta_especifica(Planta, Enfermedades) :-
    atom(Planta),
    (   planta_usos_medicinales(Planta, Enfermedades) -> true
    ;   Enfermedades = []
    ).

% PLANTAS QUE TRATAN UNA ENFERMEDAD ESPECIFICA
plantas_para_enfermedad(Enfermedad, Plantas) :-
    atom(Enfermedad),
    setof(Planta, Usos^(planta_usos_medicinales(Planta, Usos), member(Enfermedad, Usos)), Plantas).

% FORMAS DE PREPARACION
formas_preparacion_tratamiento(Formas) :-
    setof(Forma, Instrucciones^metodo_elaboracion(Forma, Instrucciones), Formas).

% MODOS DE PREPARACION DE UNA PLANTA ESPECIFICA
modo_preparacion_planta(Planta, Tecnica, Instrucciones) :-
    atom(Planta),
    forma_preparar_planta(Planta, Tecnica),
    metodo_elaboracion(Tecnica, Instr),
    normalize_ascii(Instr, Instrucciones).

% TRATAMIENTO PARA UNA ENFERMEDAD
tratamiento_enfermedad(Enfermedad, Tratamientos) :-
    atom(Enfermedad),
    setof(Planta-Tecnica-Instrucciones-Consumo,
          Usos^(
              planta_usos_medicinales(Planta, Usos),
              member(Enfermedad, Usos),
              forma_preparar_planta(Planta, Tecnica),
              metodo_elaboracion(Tecnica, Instr),
              normalize_ascii(Instr, Instrucciones),
              modo_consumo(Planta, Cons),
              normalize_ascii(Cons, Consumo)
          ),
          Tratamientos).

% ORIGENES DE LAS PLANTAS MEDICINALES
origenes_plantas_medicinales(Origenes) :-
    findall(Planta-Origen, (all_plants(Planta), origen_planta(Planta, Orig), normalize_ascii(Orig, Origen)), Origenes).

% ORIGEN DE UNA PLANTA ESPECIFICA
origen_planta_especifica(Planta, Origen) :-
    atom(Planta),
    (   origen_planta(Planta, Orig) ->
        normalize_ascii(Orig, Origen)
    ;   Origen = 'Origen no encontrado'
    ).

% BOTIQUIN DE PLANTAS
botiquin_plantas(Plantas) :-
    listar_plantas_medicinales(Plantas).

% INFORMACION COMPLETA DE UNA PLANTA
informacion_completa_planta(Planta, Detalle) :-
    atom(Planta),
    (   all_plants(Planta) ->
        (   planta_usos_medicinales(Planta, Usos) ->
            maplist(normalize_ascii, Usos, UsosNorm),
            atomics_to_string(UsosNorm, ', ', UsosTexto)
        ;   UsosTexto = 'Sin datos'
        ),
        (   caracteristicas_planta(Planta, Atributos) ->
            maplist(normalize_ascii, Atributos, AtributosNorm),
            atomics_to_string(AtributosNorm, ', ', AtributosTexto),
            findall(Def,
                    (member(Efecto, Atributos),
                     definicion_efecto(Efecto, DefTexto),
                     normalize_ascii(DefTexto, DefNorm),
                     format(atom(Def), '- ~w: ~w', [Efecto, DefNorm])),
                    Definiciones),
            (   Definiciones \= [] ->
                atomic_list_concat(Definiciones, '\n', DefinicionesTexto),
                format(atom(DefinicionesLinea), 'Definiciones de Efectos:\n~w', [DefinicionesTexto])
            ;   DefinicionesLinea = 'Definiciones de Efectos: No hay definiciones disponibles'
            )
        ;   AtributosTexto = 'Sin datos',
            DefinicionesLinea = 'Definiciones de Efectos: No hay definiciones disponibles'
        ),
        (   componentes_quimicos(Planta, Quimicos) ->
            maplist(normalize_ascii, Quimicos, QuimicosNorm),
            atomics_to_string(QuimicosNorm, ', ', QuimicosTexto)
        ;   QuimicosTexto = 'Sin datos'
        ),
        (   nombre_cientifico(Planta, NombreCientifico) ->
            normalize_ascii(NombreCientifico, NombreCientNorm)
        ;   NombreCientNorm = 'Sin datos'
        ),
        (   origen_planta(Planta, Orig) ->
            normalize_ascii(Orig, Origen)
        ;   Origen = 'Sin datos'
        ),
        (   forma_preparar_planta(Planta, Tecnica),
            metodo_elaboracion(Tecnica, Instr) ->
            normalize_ascii(Instr, Instrucciones)
        ;   Tecnica = 'Sin datos',
            Instrucciones = 'Sin datos'
        ),
        (   modo_consumo(Planta, Cons) ->
            normalize_ascii(Cons, Consumo)
        ;   Consumo = 'Sin datos'
        ),
        (   contraindicaciones_planta(Planta, Contra) ->
            normalize_ascii(Contra, Contraindicaciones)
        ;   Contraindicaciones = 'Sin datos'
        ),
        format(atom(Line1), 'Nombre: ~w', [Planta]),
        format(atom(Line2), 'Nombre Cientifico: ~w', [NombreCientNorm]),
        format(atom(Line3), 'Origen: ~w', [Origen]),
        format(atom(Line4), 'Usos Medicinales: ~w', [UsosTexto]),
        format(atom(Line5), 'Caracteristicas: ~w', [AtributosTexto]),
        format(atom(Line6), 'Componentes Quimicos: ~w', [QuimicosTexto]),
        format(atom(Line7), 'Preparacion: ~w (~w)', [Tecnica, Instrucciones]),
        format(atom(Line8), 'Consumo: ~w', [Consumo]),
        format(atom(Line9), 'Contraindicaciones: ~w', [Contraindicaciones]),
        atomic_list_concat([Line1, Line2, Line3, Line4, Line5, Line6, Line7, Line8, Line9, DefinicionesLinea], '\n', Detalle)
    ;   Detalle = 'Planta no encontrada'
    ).

