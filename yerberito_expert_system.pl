    normalize_ascii(ErrorMsg, ErrorMsgNorm),
    send(ErrorText, append, ErrorMsgNorm),
    send(DisplayDialog, append, ErrorText),
    send(DisplayDialog, open),
    send(DisplayDialog, transient_for, Dialog).

